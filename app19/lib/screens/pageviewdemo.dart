import 'package:flutter/material.dart';
import 'package:app19/screens/carbocounter.dart';
import 'package:app19/screens/evolvingimage.dart';
import 'package:app19/others/dotsindicator.dart';

class PageViewDemo extends StatefulWidget {
  @override
  _PageViewDemoState createState() => _PageViewDemoState();
}

class _PageViewDemoState extends State<PageViewDemo> {
  //With this stateful widget we build the 2 pages showed in the Homepage of the app

  final _controller = new PageController();

  static const _kDuration = const Duration(milliseconds: 300);

  static const _kCurve = Curves.ease;

  final _kArrowColor = Colors.black.withOpacity(0.8);

  //List of widgets containing the pages
  final List<Widget> _pages = <Widget>[
    CarboCounter(),
    EvolvingImage(),
  ];

  //Here we build the bottom bar with the two dots indicating the user position among the homepage pages
  @override
  Widget build(BuildContext context) {
    return new Scaffold(
      body: new IconTheme(
        data: new IconThemeData(color: _kArrowColor),
        child: new Stack(
          children: <Widget>[
            new PageView(
              controller: _controller,
              children: _pages,
            ),
            new Positioned(
              bottom: 0.0,
              left: 0.0,
              right: 0.0,
              child: new Container(
                color: Colors.grey[800]?.withOpacity(0.5),
                padding: const EdgeInsets.all(20.0),
                child: new Center(
                  child: new DotsIndicator(
                    controller: _controller,
                    itemCount: _pages.length,
                    onPageSelected: (int page) {
                      _controller.animateToPage(
                        page,
                        duration: _kDuration,
                        curve: _kCurve,
                      );
                    },
                  ),
                ),
              ),
            ),
          ],
        ),
      ),
    );
  }
}
